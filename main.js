function clickBtn1(){
	const p1 = document.getElementById("form-area1");
	const p2 = document.getElementById("mainbutton1");

	if(p1.style.display=="block"){
		// noneで非表示
		p1.style.display ="none";
	}else{
		// blockで表示
		p1.style.display ="block";
		p2.style.display ="none";
	}
}

